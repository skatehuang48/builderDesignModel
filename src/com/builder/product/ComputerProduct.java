package com.builder.product;
/**
 * 电脑产品类
 * @author Administrator
 *
 */
public class ComputerProduct {

	private String cpu;
	private String mainBord;
	private String keyBord;
	private String hardDisk;
	public String getCpu() {
		return cpu;
	}
	public void setCpu(String cpu) {
		this.cpu = cpu;
	}
	public String getMainBord() {
		return mainBord;
	}
	public void setMainBord(String mainBord) {
		this.mainBord = mainBord;
	}
	public String getKeyBord() {
		return keyBord;
	}
	public void setKeyBord(String keyBord) {
		this.keyBord = keyBord;
	}
	public String getHardDisk() {
		return hardDisk;
	}
	public void setHardDisk(String hardDisk) {
		this.hardDisk = hardDisk;
	}
	
}
